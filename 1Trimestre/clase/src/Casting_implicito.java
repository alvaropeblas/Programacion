public class Casting_implicito {
    public static void main(String[] args) throws Exception {


        //**Para datos de mayor a menor

        /*Casting implicito */
        int entero = 100;

        //Entero a long
        long num1 = entero;

        //Entero a float
        float num2 = entero;

        //entero a double
        double num3 = entero;

        System.out.println("Entero = " + entero);
        System.out.println("long = " + num1);
        System.out.println("float = " + num2);
        System.out.println("double = " + num3);
    }
}
